#include "minishell.h"

int	ft_strcmp(unsigned char *s1, unsigned char *s2)
{
	int	i;

	i = 0;
	while ((s1[i] != '\0') && s1[i] == s2[i])
		i++;
	return (s1[i] - s2[i]);
}